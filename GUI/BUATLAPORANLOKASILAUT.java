    private void Login3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Login3ActionPerformed
        // TODO add your handling code here:
        String nama = namalokasi.getText().trim();
        String koordinatStr = kordinat.getText().trim();
        String wilayahStr = wilayah.getText().trim();
        String kondisiStr = kondisi.getText().trim();

        if (nama.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama lokasi harus diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        Double koordinatVal = null;
        if (!koordinatStr.isEmpty()) {
            try {
                koordinatVal = Double.parseDouble(koordinatStr);
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(this, "Format koordinat tidak valid (contoh: -1.234567).", "Peringatan", JOptionPane.WARNING_MESSAGE);
                return;
            }
        }

        // Simpan ke tabel lokasi_laut
        String sql = "INSERT INTO lokasi_laut (nama_lokasi, koordinat, wilayah, kondisi_terakhir) VALUES (?, ?, ?, ?)";
        try (PreparedStatement ps = conn.prepareStatement(sql, Statement.RETURN_GENERATED_KEYS)) {
            ps.setString(1, nama);
            if (koordinatVal != null) {
                ps.setDouble(2, koordinatVal);
            } else {
                ps.setNull(2, Types.DECIMAL);
            }
            ps.setString(3, wilayahStr.isEmpty() ? null : wilayahStr);
            ps.setString(4, kondisiStr.isEmpty() ? null : kondisiStr);

            int affected = ps.executeUpdate();
            if (affected == 0) {
                JOptionPane.showMessageDialog(this, "Gagal menyimpan data lokasi.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            int idLokasi = -1;
            try (ResultSet keys = ps.getGeneratedKeys()) {
                if (keys != null && keys.next()) {
                    idLokasi = keys.getInt(1);
                }
            }

            JOptionPane.showMessageDialog(this,
                    "Lokasi berhasil disimpan!\nID Lokasi: " + idLokasi + "\nLanjut ke pemilihan jenis laporan.");

            // âœ… Kirim idLokasi ke JenisLaporan_A
            new JenisLaporan_A(currentUser, idLokasi).setVisible(true);
            this.dispose();

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal menyimpan data lokasi: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            logger.log(java.util.logging.Level.SEVERE, "Insert lokasi error", e);
        }
    

    
    }//GEN-LAST:event_Login3ActionPerformed

