    private void loadLaporanData() {
        try {
            String sql = """
                SELECT l.deskripsi, p.sumber_limbah, p.jenis_limbah, p.tingkat_pencemaran
                FROM laporan l
                JOIN laporan_pencemaran p ON l.id_laporan = p.id_laporan
                WHERE l.id_laporan = ?
            """;

            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setInt(1, idLaporan);
            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                jTextField7.setText(String.valueOf(idLaporan)); // ID laporan
                jTextField7.setEditable(false); // ðŸ”’ tidak bisa diedit
                jTextField6.setText(rs.getString("sumber_limbah"));
                jTextField5.setText(rs.getString("jenis_limbah"));

                String tingkat = rs.getString("tingkat_pencemaran");
                jCheckBox5.setSelected("Ringan".equalsIgnoreCase(tingkat));
                jCheckBox2.setSelected("Sedang".equalsIgnoreCase(tingkat));
                jCheckBox4.setSelected("Berat".equalsIgnoreCase(tingkat));
            } else {
                JOptionPane.showMessageDialog(this, "Data laporan tidak ditemukan!", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException e) {
            logger.log(java.util.logging.Level.SEVERE, "Gagal load data laporan", e);
        }
    }
