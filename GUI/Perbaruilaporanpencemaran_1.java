    private void Login3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Login3ActionPerformed
        // TODO add your handling code here:
        String sumber = jTextField6.getText().trim();
        String jenis = jTextField5.getText().trim();
        String tingkat = jCheckBox5.isSelected() ? "Ringan" :
                          jCheckBox2.isSelected() ? "Sedang" :
                          jCheckBox4.isSelected() ? "Berat" : null;

        if (sumber.isEmpty() || jenis.isEmpty() || tingkat == null) {
            JOptionPane.showMessageDialog(this, "Semua field wajib diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            // update tabel laporan
            String sql1 = "UPDATE laporan SET deskripsi=?, status_laporan='Menunggu' WHERE id_laporan=?";
            PreparedStatement ps1 = conn.prepareStatement(sql1);
            ps1.setString(1, "Laporan pencemaran diperbarui oleh masyarakat");
            ps1.setInt(2, idLaporan);
            ps1.executeUpdate();

            // update tabel laporan_pencemaran
            String sql2 = """
                UPDATE laporan_pencemaran
                SET sumber_limbah=?, jenis_limbah=?, tingkat_pencemaran=?
                WHERE id_laporan=?
            """;
            PreparedStatement ps2 = conn.prepareStatement(sql2);
            ps2.setString(1, sumber);
            ps2.setString(2, jenis);
            ps2.setString(3, tingkat);
            ps2.setInt(4, idLaporan);
            ps2.executeUpdate();

            JOptionPane.showMessageDialog(this, "Laporan berhasil diperbarui!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
            new Menu_Masyarakat(currentUser).setVisible(true);
        this.dispose();

        } catch (SQLException e) {
            logger.log(java.util.logging.Level.SEVERE, "Gagal update laporan", e);
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan DB: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    

 
    }//GEN-LAST:event_Login3ActionPerformed

