    // üîπ Koneksi ke database
    private void connectDatabase() {
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/pelaporan", "root", "");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Koneksi database gagal: " + e.getMessage());
        }
    }

    // üîπ Load data dari tabel user
    private void loadUserData() {
    try {
        String query = """
            SELECT u.id_user, u.nama_user, u.password, u.peran
            FROM user u
        """;

        Statement stmt = conn.createStatement();
        ResultSet rs = stmt.executeQuery(query);

        // üîπ Buat model tabel sesuai kolom yang ingin ditampilkan
        DefaultTableModel model = new DefaultTableModel(
            new Object[]{"ID User", "Nama User", "Password", "Peran"}, 0
        );

        // üîπ Masukkan data ke tabel
        while (rs.next()) {
            Object[] row = {
                rs.getInt("id_user"),
                rs.getString("nama_user"),
                rs.getString("password"),
                rs.getString("peran")
            };
            model.addRow(row);
        }

        jTable1.setModel(model);
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "‚ùå Gagal load data user: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
}

