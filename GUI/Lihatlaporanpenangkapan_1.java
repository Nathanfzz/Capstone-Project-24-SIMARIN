    private void loadTableData() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Bersihkan tabel sebelum isi ulang

        String sql = """
            SELECT l.id_laporan, l.tanggal_lapor, l.deskripsi, l.status_laporan,
                   p.jenis_kapal, p.alat_tangkap, p.zona_penangkapan, p.pelanggaran
            FROM laporan l
            JOIN laporan_penangkapan_ikan p ON l.id_pelapor = p.id_laporan
            ORDER BY l.id_laporan ASC
        """;

        try (PreparedStatement ps = conn.prepareStatement(sql);
             ResultSet rs = ps.executeQuery()) {

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("id_laporan"),
                    rs.getDate("tanggal_lapor"),
                    rs.getString("jenis_kapal"),
                    rs.getString("alat_tangkap"),
                    rs.getString("zona_penangkapan"),
                    rs.getString("pelanggaran"),
                    rs.getString("deskripsi"),
                    rs.getString("status_laporan")
                });
            }

        } catch (SQLException e) {
            logger.log(java.util.logging.Level.SEVERE, "Gagal load data laporan penangkapan", e);
            JOptionPane.showMessageDialog(this, "Gagal mengambil data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

