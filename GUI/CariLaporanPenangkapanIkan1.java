    private void Login3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Login3ActionPerformed
        // TODO add your handling code here:
        String idText = jTextField1.getText().trim();
        if (idText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Masukkan ID laporan!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try (Connection conn = DatabaseConnection.getConnection()) {
            String sql = """
                SELECT l.id_laporan, l.deskripsi, l.status_laporan,
                       li.jenis_kapal, li.alat_tangkap, li.zona_penangkapan, li.pelanggaran,
                       ll.nama_lokasi, ll.wilayah, ll.kondisi_terakhir
                FROM laporan l
                LEFT JOIN laporan_penangkapan_ikan li ON l.id_laporan = li.id_laporan
                LEFT JOIN lokasi_laut ll ON l.id_lokasi = ll.id_lokasi
                WHERE l.id_laporan = ? AND l.jenis_laporan = 'Penangkapan Ikan'
            """;
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setInt(1, Integer.parseInt(idText));
            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                String hasil = String.format("""
                    ID Laporan: %d
                    Deskripsi: %s
                    Status: %s
                    Jenis Kapal: %s
                    Alat Tangkap: %s
                    Zona Penangkapan: %s
                    Pelanggaran: %s
                    Lokasi: %s (%s)
                    Kondisi: %s
                """,
                rs.getInt("id_laporan"),
                rs.getString("deskripsi"),
                rs.getString("status_laporan"),
                rs.getString("jenis_kapal"),
                rs.getString("alat_tangkap"),
                rs.getString("zona_penangkapan"),
                rs.getString("pelanggaran"),
                rs.getString("nama_lokasi"),
                rs.getString("wilayah"),
                rs.getString("kondisi_terakhir"));

                JOptionPane.showMessageDialog(this, hasil, "Detail Laporan", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Data tidak ditemukan!", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Kesalahan DB: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "ID harus berupa angka!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    
    
    



    }//GEN-LAST:event_Login3ActionPerformed

