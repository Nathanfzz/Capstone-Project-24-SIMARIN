    // === LOAD DATA LAPORAN KE TABEL ===
    private void loadLaporanTable() {
        try {
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("""
                SELECT id_laporan, jenis_laporan, deskripsi, status_laporan
                FROM laporan
                ORDER BY id_laporan DESC
            """);

            // Buat model sederhana tanpa DefaultTableModel
            javax.swing.table.DefaultTableModel tbl = new javax.swing.table.DefaultTableModel(
                new Object[]{"ID Laporan", "Jenis", "Deskripsi", "Status"}, 0
            );

            while (rs.next()) {
                Object[] data = {
                    rs.getInt("id_laporan"),
                    rs.getString("jenis_laporan"),
                    rs.getString("deskripsi"),
                    rs.getString("status_laporan")
                };
                tbl.addRow(data);
            }

            jTable1.setModel(tbl);

            // Klik baris untuk pilih laporan
            jTable1.addMouseListener(new MouseAdapter() {
                public void mouseClicked(MouseEvent e) {
                    int row = jTable1.getSelectedRow();
                    if (row >= 0) {
                        selectedLaporanId = Integer.parseInt(jTable1.getValueAt(row, 0).toString());
                        JOptionPane.showMessageDialog(null,
                            "Laporan terpilih: ID " + selectedLaporanId,
                            "Info", JOptionPane.INFORMATION_MESSAGE);
                    }
                }
            });

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat laporan: " + e.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
            logger.log(java.util.logging.Level.SEVERE, "Error load data laporan", e);
        }
    }
    
    
        
    

