    private void nextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextActionPerformed
        // TODO add your handling code here:
         if (selectedLaporanId == -1) {
            JOptionPane.showMessageDialog(this, "⚠️ Pilih laporan terlebih dahulu dari tabel!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String hasil = txtHasil.getText().trim();
        String nominalStr = txtNominal.getText().trim();
        String statusTindak = comboStatusTindak.getSelectedItem().toString();

        if (hasil.isEmpty() || nominalStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua field wajib diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            double nominal = Double.parseDouble(nominalStr);
            String sqlInsert = """
                INSERT INTO tindak_lanjut (id_laporan, tanggal_tindak, hasil_tindak, nominal_denda, status_tindakan)
                VALUES (?, CURDATE(), ?, ?, ?)
            """;

            PreparedStatement ps = conn.prepareStatement(sqlInsert);
            ps.setInt(1, selectedLaporanId);
            ps.setString(2, hasil);
            ps.setDouble(3, nominal);
            ps.setString(4, statusTindak);
            ps.executeUpdate();

            if (statusTindak.equalsIgnoreCase("Selesai")) {
                PreparedStatement ps2 = conn.prepareStatement(
                    "UPDATE laporan SET status_laporan = 'Selesai' WHERE id_laporan = ?");
                ps2.setInt(1, selectedLaporanId);
                ps2.executeUpdate();
            }

            JOptionPane.showMessageDialog(this, "✅ Tindak lanjut berhasil disimpan!");
            loadLaporanTable();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Nominal denda harus berupa angka!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        } catch (SQLException e) {
            logger.log(java.util.logging.Level.SEVERE, "Gagal menyimpan tindak lanjut", e);
            JOptionPane.showMessageDialog(this, "Kesalahan database: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    
        
        
        
    
    
    
        
    }//GEN-LAST:event_nextActionPerformed

