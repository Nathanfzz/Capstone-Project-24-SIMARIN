    // ðŸ”¹ Fungsi untuk menampilkan data lama berdasarkan id laporan
    private void loadLaporanData() {
        try {
            String sql = """
                SELECT l.deskripsi, p.jenis_kapal, p.alat_tangkap,
                       p.zona_penangkapan, p.pelanggaran
                FROM laporan l
                JOIN laporan_penangkapan_ikan p ON l.id_laporan = p.id_laporan
                WHERE l.id_laporan = ?
                """;

            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setInt(1, idLaporan);
            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                idlaporan.setText(String.valueOf(idLaporan));
                jeniskapal.setText(rs.getString("jenis_kapal"));
                alattangkap.setText(rs.getString("alat_tangkap"));
                zonapenangkapan.setText(rs.getString("zona_penangkapan"));
                pelanggaran.setText(rs.getString("pelanggaran"));

                // ðŸ”¹ Biar ID gak bisa diubah
                idlaporan.setEditable(false);
                idlaporan.setEnabled(false);
            } else {
                JOptionPane.showMessageDialog(this, "Data laporan tidak ditemukan!", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException e) {
            logger.log(java.util.logging.Level.SEVERE, "Gagal load data laporan", e);
            JOptionPane.showMessageDialog(this, "Kesalahan database: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

