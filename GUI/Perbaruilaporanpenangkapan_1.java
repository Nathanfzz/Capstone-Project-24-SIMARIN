    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        // TODO add your handling code here:
        String jenisKapal = jeniskapal.getText().trim();
        String alatTangkap = alattangkap.getText().trim();
        String zona = zonapenangkapan.getText().trim();
        String pel = pelanggaran.getText().trim();

        if (jenisKapal.isEmpty() || alatTangkap.isEmpty() || zona.isEmpty() || pel.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua field wajib diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            // Update tabel laporan (gunakan status valid ENUM)
            String sql1 = "UPDATE laporan SET deskripsi = ?, status_laporan = ? WHERE id_laporan = ?";
            PreparedStatement ps1 = conn.prepareStatement(sql1);
            ps1.setString(1, pel);
            ps1.setString(2, "Ditindak"); // âœ… enum valid: Menunggu / Ditindak / Selesai
            ps1.setInt(3, idLaporan);
            ps1.executeUpdate();

            // Update tabel laporan_penangkapan_ikan
            String sql2 = """
                UPDATE laporan_penangkapan_ikan
                SET jenis_kapal = ?, alat_tangkap = ?, zona_penangkapan = ?, pelanggaran = ?
                WHERE id_laporan = ?
                """;
            PreparedStatement ps2 = conn.prepareStatement(sql2);
            ps2.setString(1, jenisKapal);
            ps2.setString(2, alatTangkap);
            ps2.setString(3, zona);
            ps2.setString(4, pel);
            ps2.setInt(5, idLaporan);
            ps2.executeUpdate();

            JOptionPane.showMessageDialog(this, "âœ… Laporan berhasil diperbarui!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            new Menu_Masyarakat(currentUser).setVisible(true);
            this.dispose();
            } catch (SQLException e) {
            logger.log(java.util.logging.Level.SEVERE, "Gagal update laporan", e);
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan DB: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    
    
    }//GEN-LAST:event_loginActionPerformed

