    // üîπ Menampilkan data user dari tabel `user`
    private void loadData() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID User");
        model.addColumn("Nama User");
        model.addColumn("No HP");
        model.addColumn("Peran");

        try {
            String sql = "SELECT id_user, nama_user, no_hp, peran FROM user";
            PreparedStatement ps = conn.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("id_user"),
                    rs.getString("nama_user"),
                    rs.getString("no_hp"),
                    rs.getString("peran")
                });
            }
            jTable1.setModel(model);
        } catch (SQLException e) {
            logger.log(java.util.logging.Level.SEVERE, "Gagal memuat data user", e);
            JOptionPane.showMessageDialog(this, "Kesalahan DB: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    // üîπ Klik baris tabel ‚Üí tampilkan peran di comboBox
    private void setupTableClickListener() {
        jTable1.getSelectionModel().addListSelectionListener(event -> {
            if (!event.getValueIsAdjusting() && jTable1.getSelectedRow() != -1) {
                String peranSekarang = jTable1.getValueAt(jTable1.getSelectedRow(), 3).toString();
                jComboBox1.setSelectedItem(peranSekarang);
            }
        });
    }

    // üîπ Update peran user berdasarkan id_user
    private void updatePeranUser(int idUser, String peranBaru) {
        try {
            String sql = "UPDATE user SET peran = ? WHERE id_user = ?";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, peranBaru);
            ps.setInt(2, idUser);

            int rows = ps.executeUpdate();
            if (rows > 0) {
                JOptionPane.showMessageDialog(this, "‚úÖ Peran user berhasil diperbarui menjadi: " + peranBaru);
                jTable1.setValueAt(peranBaru, jTable1.getSelectedRow(), 3);
            } else {
                JOptionPane.showMessageDialog(this, "‚ö†Ô∏è ID user tidak ditemukan!");
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "‚ùå Gagal memperbarui peran: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }


    

