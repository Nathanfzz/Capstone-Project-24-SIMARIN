    private void loadTableData() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID Laporan");
        model.addColumn("Tanggal Lapor");
        model.addColumn("Deskripsi");
        model.addColumn("Jenis Kapal");
        model.addColumn("Alat Tangkap");
        model.addColumn("Zona Penangkapan");
        model.addColumn("Pelanggaran");
        model.addColumn("Status");

        try {
            String sql = """
                SELECT l.id_laporan, l.tanggal_lapor, l.deskripsi,
                       p.jenis_kapal, p.alat_tangkap, p.zona_penangkapan, p.pelanggaran,
                       l.status_laporan
                FROM laporan l
                JOIN laporan_penangkapan_ikan p ON l.id_laporan = p.id_laporan
                WHERE l.id_pelapor = ? AND l.jenis_laporan = 'Penangkapan Ikan'
                ORDER BY l.tanggal_lapor DESC
            """;
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setInt(1, currentUser.getIdUser());
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("id_laporan"),
                    rs.getDate("tanggal_lapor"),
                    rs.getString("deskripsi"),
                    rs.getString("jenis_kapal"),
                    rs.getString("alat_tangkap"),
                    rs.getString("zona_penangkapan"),
                    rs.getString("pelanggaran"),
                    rs.getString("status_laporan")
                });
            }
            jTable1.setModel(model);
        } catch (SQLException e) {
            logger.log(java.util.logging.Level.SEVERE, "Gagal load data laporan", e);
            JOptionPane.showMessageDialog(this, "Kesalahan DB: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    


