    // Method untuk load data laporan dan gambar
    private void loadData() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID Laporan");
        model.addColumn("ID Pelapor");
        model.addColumn("ID Lokasi");
        model.addColumn("Tanggal Lapor");
        model.addColumn("Deskripsi");
        model.addColumn("Status Laporan");
        model.addColumn("Jenis Laporan");
        model.addColumn("Foto Bukti");

        try {
            String sql = "SELECT id_laporan, id_pelapor, id_lokasi, tanggal_lapor, deskripsi, status_laporan, jenis_laporan, foto_bukti FROM laporan";
            PreparedStatement ps = conn.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                byte[] imgBytes = rs.getBytes("foto_bukti"); // Ambil gambar dari BLOB
                ImageIcon imageIcon = null;
                if (imgBytes != null) {
                    imageIcon = new ImageIcon(imgBytes);
                }
                
                model.addRow(new Object[]{
                    rs.getInt("id_laporan"),
                    rs.getInt("id_pelapor"),
                    rs.getInt("id_lokasi"),
                    rs.getDate("tanggal_lapor"),
                    rs.getString("deskripsi"),
                    rs.getString("status_laporan"),
                    rs.getString("jenis_laporan"),
                    imageIcon != null ? imageIcon : null // Set gambar ke tabel
                });
            }
            jTable1.setModel(model);

        } catch (SQLException e) {
            logger.log(java.util.logging.Level.SEVERE, "Gagal memuat data laporan", e);
            JOptionPane.showMessageDialog(this, "Kesalahan DB: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }


