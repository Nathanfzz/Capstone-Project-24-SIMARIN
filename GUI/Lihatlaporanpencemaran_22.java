    private void Login4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Login4ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
    if (selectedRow != -1) {
        int reportId = Integer.parseInt(jTable1.getValueAt(selectedRow, 0).toString());

        // Mengambil gambar dari database berdasarkan ID laporan
        byte[] imageBytes = getImageFromDatabase(reportId);
        if (imageBytes != null) {
            // Mengonversi byte[] menjadi ImageIcon
            ImageIcon imageIcon = new ImageIcon(imageBytes);
            // Membuat label untuk menampilkan gambar
            JLabel label = new JLabel(imageIcon);
            JOptionPane.showMessageDialog(this, label, "Foto Bukti Pencemaran", JOptionPane.PLAIN_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Tidak ada gambar untuk laporan ini.", 
                "Tidak Ada Gambar", JOptionPane.INFORMATION_MESSAGE);
        }
    } else {
        JOptionPane.showMessageDialog(this, "Pilih laporan untuk menampilkan gambar terlebih dahulu!", 
            "Peringatan", JOptionPane.WARNING_MESSAGE);
    }
}

// Metode untuk mengambil gambar dari database berdasarkan ID laporan
private byte[] getImageFromDatabase(int reportId) {
    byte[] imageBytes = null;
    try {
        // Query untuk mengambil gambar berdasarkan ID laporan
        String sql = "SELECT foto_bukti FROM laporan WHERE id_laporan = ?";
        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setInt(1, reportId);
        ResultSet rs = ps.executeQuery();

        // Jika gambar ditemukan, ambil data gambar sebagai byte[]
        if (rs.next()) {
            imageBytes = rs.getBytes("foto_bukti"); // Kolom foto_bukti
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Gagal mengambil gambar dari database: " + e.getMessage(),
            "Error", JOptionPane.ERROR_MESSAGE);
    }
    return imageBytes;



    

        
    }//GEN-LAST:event_Login4ActionPerformed

