    private void loadData() {
         DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID Lokasi");
    model.addColumn("Nama Lokasi");
    model.addColumn("Koordinat");
    model.addColumn("Wilayah");
    model.addColumn("Kondisi Terakhir");

    try {
        String sql = "SELECT id_lokasi, nama_lokasi, koordinat, wilayah, kondisi_terakhir FROM lokasi_laut";
        PreparedStatement ps = conn.prepareStatement(sql);
        ResultSet rs = ps.executeQuery();

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id_lokasi"),
                rs.getString("nama_lokasi"),
                rs.getString("koordinat"),
                rs.getString("wilayah"),
                rs.getString("kondisi_terakhir")
            });
        }

        jTable1.setModel(model);

    } catch (SQLException e) {
        logger.log(java.util.logging.Level.SEVERE, "Gagal memuat data lokasi laut", e);
        JOptionPane.showMessageDialog(this, 
            "Kesalahan DB: " + e.getMessage(), 
            "Error", 
            JOptionPane.ERROR_MESSAGE);
    }
}


