    private void loadData() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID Tindak");
        model.addColumn("ID Laporan");
        model.addColumn("Tanggal Tindak");
        model.addColumn("Hasil Tindak");
        model.addColumn("Nominal Denda");
        model.addColumn("Status Tindakan");

        try {
            String sql = "SELECT id_tindak, id_laporan, tanggal_tindak, hasil_tindak, nominal_denda, status_tindakan FROM tindak_lanjut";
            PreparedStatement ps = conn.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("id_tindak"),
                    rs.getInt("id_laporan"),
                    rs.getDate("tanggal_tindak"),
                    rs.getString("hasil_tindak"),
                    rs.getDouble("nominal_denda"),
                    rs.getString("status_tindakan")
                });
            }
            jTable1.setModel(model);

        } catch (SQLException e) {
            logger.log(java.util.logging.Level.SEVERE, "Gagal memuat data tindak lanjut", e);
            JOptionPane.showMessageDialog(this, "Kesalahan DB: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }


