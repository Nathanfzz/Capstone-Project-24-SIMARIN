    private void sendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendActionPerformed
        // TODO add your handling code here:
                                             
        String deskripsi = jTextField4.getText().trim();
        String jenisIkan = jTextField1.getText().trim();
        String jumlahTangkap = jTextField2.getText().trim();
        String alatTangkap = jTextField3.getText().trim();

        if (deskripsi.isEmpty() || jenisIkan.isEmpty() || jumlahTangkap.isEmpty() || alatTangkap.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua field harus diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String sql = "INSERT INTO laporan (id_pelapor, id_lokasi, tanggal_lapor, deskripsi, jenis_laporan, status_laporan) "
                   + "VALUES (?, ?, ?, ?, ?, ?)";

        try (PreparedStatement ps = conn.prepareStatement(sql)) {
            ps.setInt(1, currentUser.getIdUser());
            ps.setInt(2, lokasiId);
            ps.setDate(3, java.sql.Date.valueOf(java.time.LocalDate.now()));
            ps.setString(4, deskripsi + " | Jenis Ikan: " + jenisIkan + ", Jumlah: " + jumlahTangkap + ", Alat: " + alatTangkap);
            ps.setString(5, "Penangkapan Ikan");
            ps.setString(6, "Menunggu");

            int rows = ps.executeUpdate();
            if (rows > 0) {
                JOptionPane.showMessageDialog(this, "‚úÖ Laporan penangkapan ikan berhasil dikirim!");
                new Menu_Admin(currentUser).setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "‚ùå Gagal mengirim laporan!", "Error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (SQLException e) {
            logger.log(java.util.logging.Level.SEVERE, "SQL Error", e);
            JOptionPane.showMessageDialog(this, "Kesalahan SQL: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_sendActionPerformed

