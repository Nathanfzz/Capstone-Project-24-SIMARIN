    private void Login3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Login3ActionPerformed
        // TODO add your handling code here:
        String deskripsi = jTextField4.getText().trim();
        String jenisLimbah = jTextField5.getText().trim();
        String fotoPath = jTextField6.getText().trim();
        String tingkat = null;

        if (jCheckBox5.isSelected()) tingkat = "Ringan";
        else if (jCheckBox1.isSelected()) tingkat = "Sedang";
        else if (jCheckBox4.isSelected()) tingkat = "Berat";

        // Validasi dasar
        if (deskripsi.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Deskripsi tidak boleh kosong!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (lokasiId <= 0) {
            JOptionPane.showMessageDialog(this, "Data lokasi tidak valid. Silakan buat lokasi terlebih dahulu!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (tingkat == null) {
            JOptionPane.showMessageDialog(this, "Pilih tingkat pencemaran terlebih dahulu!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String sql = "INSERT INTO laporan (id_pelapor, id_lokasi, tanggal_lapor, deskripsi, foto_bukti, status_laporan, jenis_laporan) "
                   + "VALUES (?, ?, ?, ?, ?, ?, ?)";

        try (PreparedStatement ps = conn.prepareStatement(sql);
             FileInputStream fis = (fotoPath.isEmpty()) ? null : new FileInputStream(new File(fotoPath))) {

            ps.setInt(1, currentUser.getIdUser());
            ps.setInt(2, lokasiId);
            ps.setDate(3, Date.valueOf(LocalDate.now()));
            ps.setString(4, deskripsi);

            if (fis != null) {
                ps.setBinaryStream(5, fis, (int) new File(fotoPath).length());
            } else {
                ps.setNull(5, Types.BLOB);
            }

            ps.setString(6, "Menunggu");
            ps.setString(7, "Pencemaran Laut");

            int inserted = ps.executeUpdate();

            if (inserted > 0) {
                JOptionPane.showMessageDialog(this, "✅ Laporan berhasil dikirim!");
                new Menu_Masyarakat(currentUser).setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "❌ Gagal mengirim laporan.", "Error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Kesalahan SQL: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            logger.log(java.util.logging.Level.SEVERE, "SQL Error", e);
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Kesalahan saat membaca file: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            logger.log(java.util.logging.Level.SEVERE, "File IO Error", e);
        }
    


    
    
    }//GEN-LAST:event_Login3ActionPerformed

