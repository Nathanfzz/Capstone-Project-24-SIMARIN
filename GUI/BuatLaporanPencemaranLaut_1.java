    private void uploadfotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_uploadfotoActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
    fileChooser.setDialogTitle("Pilih Foto Bukti Pencemaran Laut");
    fileChooser.setFileSelectionMode(JFileChooser.FILES_ONLY);

    int result = fileChooser.showOpenDialog(this);
    if (result == JFileChooser.APPROVE_OPTION) {
        File selectedFile = fileChooser.getSelectedFile();

        // Validasi ekstensi file gambar
        String fileName = selectedFile.getName().toLowerCase();
        if (!(fileName.endsWith(".jpg") || fileName.endsWith(".jpeg") || 
              fileName.endsWith(".png") || fileName.endsWith(".gif"))) {
            JOptionPane.showMessageDialog(this, "File harus berupa gambar (jpg, jpeg, png, gif)!",
                "Format Tidak Didukung", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Validasi ukuran file
        long fileSizeInBytes = selectedFile.length();
        long maxSizeInBytes = 2 * 1024 * 1024; // Maksimal 2 MB
        if (fileSizeInBytes > maxSizeInBytes) {
            JOptionPane.showMessageDialog(this, "Ukuran file terlalu besar! Maksimal 2 MB.",
                "File Terlalu Besar", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Tampilkan path file di JTextField
        jTextField6.setText(selectedFile.getAbsolutePath());

        try {
            // Membaca file gambar sebagai byte array
            FileInputStream fis = new FileInputStream(selectedFile);
            ByteArrayOutputStream baos = new ByteArrayOutputStream();
            byte[] buffer = new byte[1024];
            int length;
            
            while ((length = fis.read(buffer)) != -1) {
                baos.write(buffer, 0, length);
            }

            byte[] imageBytes = baos.toByteArray(); // Gambar dalam bentuk byte[]

            // Proses menyimpan byte[] imageBytes ke database di kolom gambar atau BLOB
            saveImageToDatabase(imageBytes);
            
            JOptionPane.showMessageDialog(this, "Foto berhasil diupload!", "Upload Sukses", JOptionPane.INFORMATION_MESSAGE);

        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error dalam membaca file gambar: " + e.getMessage(), 
                "Error", JOptionPane.ERROR_MESSAGE);
        }
    } else {
        JOptionPane.showMessageDialog(this, "Pemilihan foto dibatalkan.", "Info", JOptionPane.INFORMATION_MESSAGE);
    }
}

// Metode untuk menyimpan gambar ke database
private void saveImageToDatabase(byte[] imageBytes) {
    // Simpan gambar ke dalam database
    try {
        String sql = "INSERT INTO laporan (gambar) VALUES (?)";
        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setBytes(1, imageBytes);
        ps.executeUpdate();
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Gagal menyimpan gambar ke database: " + e.getMessage(),
            "Error", JOptionPane.ERROR_MESSAGE);
    }



    
    }//GEN-LAST:event_uploadfotoActionPerformed

