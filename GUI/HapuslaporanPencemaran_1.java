    private void loadLaporanUser() {
        DefaultTableModel model = new DefaultTableModel(
                new Object[][]{},
                new String[]{"ID Laporan", "Deskripsi", "Status"}
        );
        jTable1.setModel(model);

        // Hanya ambil laporan milik user dengan jenis Pencemaran Laut
        String sql = """
            SELECT id_laporan, deskripsi, status_laporan 
            FROM laporan 
            WHERE id_pelapor = ? AND jenis_laporan = 'Pencemaran Laut'
        """;

        try (PreparedStatement ps = conn.prepareStatement(sql)) {
            ps.setInt(1, currentUser.getIdUser());
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                model.addRow(new Object[]{
                        rs.getInt("id_laporan"),
                        rs.getString("deskripsi"),
                        rs.getString("status_laporan")
                });
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this,
                    "Gagal memuat data: " + e.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }


