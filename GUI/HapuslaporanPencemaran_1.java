    private void Login3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Login3ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih laporan yang ingin dihapus terlebih dahulu!");
            return;
        }

        int idLaporan = (int) jTable1.getValueAt(selectedRow, 0);
        int confirm = JOptionPane.showConfirmDialog(this,
                "Yakin ingin menghapus laporan ID " + idLaporan + "?",
                "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            hapusLaporan(idLaporan);
        }
    }

    private void hapusLaporan(int idLaporan) {
        String sql = "DELETE FROM laporan WHERE id_laporan = ? AND id_pelapor = ?";
        try (PreparedStatement ps = conn.prepareStatement(sql)) {
            ps.setInt(1, idLaporan);
            ps.setInt(2, currentUser.getIdUser());
            int rows = ps.executeUpdate();

            if (rows > 0) {
                JOptionPane.showMessageDialog(this, "Laporan berhasil dihapus!");
                loadLaporanUser();
            } else {
                JOptionPane.showMessageDialog(this, "Tidak dapat menghapus laporan ini (bukan milik Anda).");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this,
                    "Gagal menghapus laporan: " + e.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    
    
    
    

    }//GEN-LAST:event_Login3ActionPerformed

